SELECT T.CAR_ID, T.CAR_TYPE, ROUND(T.DAILY_FEE*30*(100-B.DISCOUNT_RATE)/100) AS FEE
FROM CAR_RENTAL_COMPANY_CAR AS T
JOIN CAR_RENTAL_COMPANY_RENTAL_HISTORY AS A ON T.CAR_ID=A.CAR_ID
JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN AS B ON T.CAR_TYPE=B.CAR_TYPE
WHERE T.CAR_ID NOT IN (
    SELECT CAR_ID
    FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
    WHERE END_DATE > '2022-11-01' AND START_DATE < '2022-12-01'
) AND B.DURATION_TYPE='30일 이상'
GROUP BY T.CAR_ID
HAVING T.CAR_TYPE IN ('세단', 'SUV') AND (FEE>=500000 AND FEE<2000000)
ORDER BY FEE DESC, CAR_TYPE, CAR_ID DESC